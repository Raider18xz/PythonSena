import Matema


# Valor absoluto
numero = -10
resultado = Matema.valor_absoluto(numero)
print(f"El valor absoluto de {numero} es: {resultado}")

# Raíz cuadrada
numero = 16
resultado = Matema.raiz_cuadrada(numero)
print(f"La raíz cuadrada de {numero} es: {resultado}")

# Seno
grados = 45  # Ángulo en grados
resultado = Matema.calcular_seno(grados)
print(f"El seno de {grados} grados es: {resultado}")

# Exponencial
numero = 2
resultado = Matema.calcular_exponencial(numero)
print(f"El valor exponencial de {numero} es: {resultado}")

# Factorial
numero = 5
resultado = Matema.calcular_factorial(numero)
print(f"El factorial de {numero} es: {resultado}")
